function bubbleSort(arr) {
  let swapped;

  do {
    swapped = false;
    for (let i = 0; i < arr.length - 1; i++) {
      if (arr[i] > arr[i + 1]) {
        // Swap
        let temp = arr[i];
        arr[i] = arr[i + 1];
        arr[i + 1] = temp;
        swapped = true;
      }
    }
  } while (swapped);

  return arr;
}

let numbers = [4, 2, 8, 1, 6];
let sortedNumbers = bubbleSort(numbers.slice());
console.log("Sorted using Bubble Sort:", sortedNumbers);
